def sum(a, b):
    """
    Calcule la somme de deux nombres.

    Args:
        a (int ou float): Le premier nombre.
        b (int ou float): Le deuxième nombre.

    Returns:
        int ou float: La somme des deux nombres.
    """
    return a + b


def subtraction(a, b):
    """
    Calcule la soustraction de deux nombres.

    Args:
        a (int ou float): Le premier nombre.
        b (int ou float): Le deuxième nombre.

    Returns:
        int ou float: Le résultat de la soustraction (a - b).
    """
    return a - b
